a = {}
b = 'груши 45 991 63 100 12 морковь 13 47 26 0 16'
b = b.split()
a["Продукт 1"] = b[0]
a['продажи 1'] = []
for i in b[1:5]:
    a['продажи 1'].append(int(i))
a['продукт 2'] = b[6]
a['продажи 2'] = []
for i in b[7:11]:
    a['продажи 2'].append(int(i))
print(a)

min1 = min(a['продажи 1'])
min2 = min(a['продажи 2'])
print(f"Минимальные продажи груш: {min1}")
print(f"Минимальные продажи моркови: {min2}")
